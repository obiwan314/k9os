 # Get a center latitude,longitude from an array of like geopoints
 #
 # @param array data 2 dimensional array of latitudes and longitudes
 # For Example:
 # $data = array
 # (
 #    *   0 = > array(45.849382, 76.322333),
 #              *   1 = > array(45.843543, 75.324143),
 #                        *   2 = > array(45.765744, 76.543223),
 #                                  *   3 = > array(45.784234, 74.542335)
 #                                            * );
import math

def centerOfEllipsis(data):
    maxx=0
    minx=0
    maxy=0
    miny=0

    for coord in data:
        if coord[0]>maxx:
            maxx=coord[0]
        if coord[0]<minx:
            minx=coord[0]
        if coord[1]>maxy:
            maxy=coord[1]
        if coord[1]<miny:
            miny=coord[1]

    cy=(maxy-miny)/2+miny

    cx=(maxx-minx)/2+minx

    return [cx,cy]


def GetCenterFromDegrees(data):

    num_coords = len(data)

    X = 0.0
    Y = 0.0
    Z = 0.0

    for coord in data:
        lat = coord[0] * math.pi / 180
        lon = coord[1] * math.pi / 180

        a = math.cos(lat) * math.cos(lon)
        b = math.cos(lat) * math.sin(lon)
        c = math.sin(lat)

        X += a
        Y += b
        Z += c


    X = X/num_coords
    Y = Y/num_coords
    Z = Z/num_coords

    lon = math.atan2(Y, X)
    hyp = math.sqrt(X * X + Y * Y)
    lat = math.atan2(Z, hyp)

    return [lat * 180 / math.pi, lon * 180 / math.pi];

